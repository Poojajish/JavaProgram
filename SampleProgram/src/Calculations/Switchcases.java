package Calculations;

public class Switchcases {
	public static void main(String args[])
	{
	String name="pooja";
	switch(name)
	{
	case"pooja":
	System.out.println("name is pooja");
	break;

	case"revathy":

	System.out.println("name is revathy");

	break;
	case "renuka":

	System.out.println("name is renuka");
	break;
	default:
	System.out.println("invalid");
	}
	}
	

}
