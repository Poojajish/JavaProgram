package Calculations;

public class IfElseIfLadder {
	public static void main(String args[])
	{
		int age=18;
		
		if(age==20)
		{
			System.out.println("age is 20");
		}
		else if(age<20)
		{
			System.out.println("true");
			
		}
		else if(age==25)
		{
			System.out.println("age is 25");
		}
		else
		{
			System.out.println("invalid condition");
		}
	}

}
